Msg* MsgPool::DecodeMsg(CBufferReader &reader) {
	int32_t msg_id = 0;
	if (!reader.GetInt32(msg_id)) {
		return NULL;
	}
	if (msg_id == 0) {
		return NULL;
	}
	map<uint32_t, CreateFun>::iterator itr = pool_.find(msg_id);
	if (itr == pool_.end()) {
		return NULL;
	}
	CreateFun fun = itr->second;
	Msg *nptr = fun();
	if (nptr != NULL) {
		del_.push_back(nptr);
	}
	return nptr;
}

bool MsgPool::EncodeMsg(CBufferWriter &writer, Msg* ptr) {
	if (ptr == NULL) {
		PACKET_ENCODE(Int32, 0);
		return true;
	}
	PACKET_ENCODE(Int32, ptr->msgId());
	if (!ptr->encode(writer)) {
		return false;
	}
	return true;
}

void MsgPool::Recycle() {
	if (del_.empty()) {
		return;
	}
	for (vector<Msg*>::iterator itr = del_.begin(); itr != del_.end(); ++itr) {
		Msg *ptr = *itr;
		if (ptr != NULL) {
			delete ptr;
			*itr = NULL;
		}
	}
	debug_log("recycle seize %u", del_.size());
	del_.clear();
}

