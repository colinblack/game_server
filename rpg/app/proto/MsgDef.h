class Point: public Msg {
public:
	int x;
	int y;
	union {
		int sceneId;
		int entityId;
		int npcID;
	};

	Point(int x, int y) {
		this->x = x;
		this->y = y;
		sceneId = 0;
	}

	Point() : x(0), y(0), sceneId(0) {
	}

	bool operator ==(const Point &other) {
		return ((this->x == other.x) && (this->y == other.y));
	}

	bool operator !=(const Point& other) {
		return (this->x != other.x) || (this->y != other.y);
	}

	Point& operator =(const Point& other) {
		x = other.x;
		y = other.y;
		return *this;
	}

	virtual bool decode(CBufferReader &reader) {
		PACKET_DECODE(Int32, x);
		PACKET_DECODE(Int32, y);
		return true;
	}
	virtual bool encode(CBufferWriter &writer) const {
		PACKET_ENCODE(Int32, x);
		PACKET_ENCODE(Int32, y);
		return true;
	}
	virtual void clear() {
		x = 0;
		y = 0;
		sceneId = 0;
	}
};

class Identity: public Msg {
public:
	enum {
		ROLE_TYPE_BEGIN = 110
	};
	byte type_;
	byte proxy_;
	uint16_t server_;
	int32_t id_;
	virtual bool decode(CBufferReader &reader) {
		PACKET_DECODE(Byte, type_);
		PACKET_DECODE(Byte, proxy_);
		PACKET_DECODE(UInt16, server_);
		PACKET_DECODE(Int32, id_);
		return true;
	}
	virtual bool encode(CBufferWriter &writer) const {
		PACKET_ENCODE(Byte, type_);
		PACKET_ENCODE(Byte, proxy_);
		PACKET_ENCODE(UInt16, server_);
		PACKET_ENCODE(Int32, id_);
		return true;
	}
	virtual void clear() {
		type_ = 0;
		proxy_ = 0;
		server_ = 0;
		id_ = 0;
	}
	byte roleId() {
		return type_ > ROLE_TYPE_BEGIN ? type_ - ROLE_TYPE_BEGIN : 0;
	}

	bool operator<(const Identity &oth) const {
		return id_ < oth.id_;
	}
	bool operator<=(const Identity &oth) const {
		return id_ <= oth.id_;
	}
	bool operator>(const Identity &oth) const {
		return id_ > oth.id_;
	}
	bool operator>=(const Identity &oth) const {
		return id_ >= oth.id_;
	}
	bool operator==(const Identity &oth) const {
		return id_ == oth.id_;
	}
};

